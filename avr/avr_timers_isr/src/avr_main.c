#define F_CPU 16000000UL
#include <avr/io.h>
#include <avr/interrupt.h> // Заголовочный файл для работы с прерываниями

// Глобальная переменная-счётчик для отсчёта времени.
volatile uint16_t timer_counter = 0;
// Флаг-переменная для переключения светодиода каждые 500 мс.
volatile uint8_t led_toggle_flag = 0;

// Обработчик прерывания по сравнению таймера 1 (Timer/Counter1 Compare Match A)
ISR(TIMER1_COMPA_vect) {
    timer_counter++;
    if(timer_counter >= 50) { // 50 * 10мс = 500 мс
        timer_counter = 0;
        led_toggle_flag = 1; // Устанавливаем флаг
    }
}

int main(void) {
    // 1. Настройка пина PB5 на выход
    DDRB |= (1 << DDB5);

    // 2. Настройка таймера 1 (16-битный) в режиме CTC (Clear Timer on Compare)
    // Предделитель = 64. Частота тактирования таймера = 16 МГц / 64 = 250 кГц.
    // Период тика таймера = 1 / 250 кГц = 4 микросекунды.
    // Мы хотим прерывание каждые 10 мс (0.01 сек).
    // Количество тиков для 10 мс = 0.01 сек / 4 мкс = 2500 тиков.
    // Устанавливаем регистр сравнения OCR1A в 2500.
    TCCR1B |= (1 << WGM12); // Режим CTC (WGM12=1)
    TCCR1B |= (1 << CS11) | (1 << CS10); // Предделитель = 64 (CS11=1, CS10=1)
    OCR1A = 2500 - 1; // -1 потому что счёт начинается с 0.
    TIMSK1 |= (1 << OCIE1A); // Разрешаем прерывание по совпадению с OCR1A

    // 3. Глобально разрешаем прерывания
    sei();

    // 4. Главный цикл теперь свободен для других задач.
    // Он просто проверяет флаг и переключает светодиод.
    while (1) {
        if(led_toggle_flag) {
            led_toggle_flag = 0; // Сбрасываем флаг
            PORTB ^= (1 << PORTB5); // Используем XOR для переключения состояния бита (включить/выключить)
        }
        // Здесь можно выполнять любую другую полезную работу, не блокируя систему.
    }
}
