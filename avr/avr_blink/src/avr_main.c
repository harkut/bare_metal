// Файл: avr_main.c
// Определение частоты микроконтроллера (16 МГц для Arduino)
#define F_CPU 16000000UL
// Подключение заголовочных файлов.
// <avr/io.h> содержит определения всех регистров для вашего МК (например, DDRB, PORTB).
// <util/delay.h> содержит функции для работы с задержками.
#include <avr/io.h>
#include <util/delay.h>

int main(void) {
  // Настройка пина PB5 на выход (Data Direction Register B, bit 5).
  // Запись '1' в бит DDRB5 (пятый бит регистра DDRB) означает, что пин PB5 будет выходом.
  DDRB |= (1 << DDB5); // То же самое, что DDRB |= _BV(PB5); или DDRB |= 0b00100000;

  // Бесконечный цикл, в котором работает программа.
  while (1) {
    // Установить вывод PB5 в высокий уровень (логическая '1', включить светодиод).
    // Запись '1' в бит PORTB5 регистра PORTB устанавливает высокий уровень на физическом выводе.
    PORTB |= (1 << PORTB5);
    // Задержка 500 миллисекунд.
    _delay_ms(1000);

    // Установить вывод PB5 в низкий уровень (логический '0', выключить светодиод).
    // Запись '0' в бит PORTB5 регистра PORTB устанавливает низкий уровень.
    PORTB &= ~(1 << PORTB5);
    _delay_ms(1000);
  }
  // Программа никогда не дойдёт до этой точки из-за бесконечного цикла while(1).
  return 0;
}
